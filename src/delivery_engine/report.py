"""delivery_engine.report - the deterministic visual report.

Step 21. The engine's generated eda_notebook.ipynb is the source of
truth: hashed findings, injected numbers. This module reads those same
findings and renders a professional, self-contained HTML report whose
charts are drawn ONLY from numbers already in the store.

THE GOVERNING PRINCIPLE: charts are cognitive scaffolding, not
conclusions. A chart provokes the right question and points the reader
at the evidence; it never answers for them, and it never computes. This
is the injected-numbers rule made visual - the report DRAWS hashed
findings, it does not calculate, estimate, or decide.

Determinism contract: the report is a pure function of the findings.
Every chart is inline SVG generated by fixed code from the findings
values - no randomness, no timestamps in the rendered region, no
network, no AI at runtime. The same findings produce byte-identical
HTML, so a reviewer can re-perform the report exactly and verify every
drawn number against the store.

Adaptivity WITHOUT judgement: which charts appear is decided by a fixed
rulebook keyed off the deterministic column profile the engine already
produced - more columns make a taller profile chart, and so on. The
data shapes the report through the hashed profile, never through an LLM
reading the data.

v1 draws only the three families whose numbers already exist in the
store: the DAMA scorecard, the validation results, and the per-column
profile shape. Richer target-based charts (class balance, rate by
segment) are a declared future extension that first requires a
deterministic stage to compute-and-hash those numbers - drawing them
today would require calculation, which the constitution forbids.
"""

from __future__ import annotations

import html
import json
from pathlib import Path
from typing import Any, Final

__all__ = ["ReportError", "build_report_html", "report_from_package"]

# Fixed professional palette (disclosed constants, never chosen at
# runtime). Colour never carries meaning alone - every chart pairs
# colour with a text label or value, per WCAG 1.4.1.
_INK: Final = "#1a2b4a"
_MUTED: Final = "#5b6b82"
_GOOD: Final = "#2e7d5b"
_WARN: Final = "#b45309"
_BAR_BG: Final = "#e8edf3"
_GRID: Final = "#d4dce6"
_ACCENT: Final = "#2b5c8a"

_CHART_W: Final = 640
_ROW_H: Final = 30
_BAR_H: Final = 18


class ReportError(Exception):
    """A report could not be built, stated cleanly with the reason."""


def _esc(text: object) -> str:
    """HTML-escape any value for safe embedding."""
    return html.escape(str(text), quote=True)


def _pct(value: float) -> str:
    """Deterministic percent label, 1 decimal - matches the notebook."""
    return f"{value * 100:.1f}%"


# ── charts: pure functions, findings values -> inline SVG ────────────────────


def _bar_row(y: int, label: str, value_label: str, frac: float, colour: str) -> str:
    """One horizontal bar with its label and value, as SVG.

    frac is a display width in [0, 1]; it is taken directly from a
    findings value, never computed here beyond scaling to pixels.
    """
    frac = max(0.0, min(1.0, frac))
    bar_w = round(frac * (_CHART_W - 220))
    text_y = y + _BAR_H - 4
    return (
        f'<text x="0" y="{text_y}" class="lbl">{_esc(label)}</text>'
        f'<rect x="150" y="{y}" width="{_CHART_W - 220}" '
        f'height="{_BAR_H}" fill="{_BAR_BG}" rx="3"/>'
        f'<rect x="150" y="{y}" width="{bar_w}" height="{_BAR_H}" '
        f'fill="{colour}" rx="3"/>'
        f'<text x="{_CHART_W - 60}" y="{text_y}" class="val">'
        f"{_esc(value_label)}</text>"
    )


def _svg(height: int, rows: str, title: str) -> str:
    """Wrap chart rows in an accessible SVG (role img + title/desc)."""
    return (
        f'<svg viewBox="0 0 {_CHART_W} {height}" '
        f'xmlns="http://www.w3.org/2000/svg" role="img" '
        f'aria-label="{_esc(title)}" class="chart">'
        f"<title>{_esc(title)}</title>{rows}</svg>"
    )


def _dama_chart(dama: dict[str, float | None]) -> str:
    """DAMA scorecard as bars. None ('not scored') is shown distinctly
    - never drawn as zero, because absence of a score is not a score of
    zero (the accuracy/timeliness honesty the engine insists on)."""
    rows = ""
    y = 8
    for dim in sorted(dama):
        val = dama[dim]
        if val is None:
            rows += (
                f'<text x="0" y="{y + _BAR_H - 4}" class="lbl">'
                f"{_esc(dim)}</text>"
                f'<rect x="150" y="{y}" width="{_CHART_W - 220}" '
                f'height="{_BAR_H}" fill="none" stroke="{_GRID}" '
                f'stroke-dasharray="4 3" rx="3"/>'
                f'<text x="{_CHART_W - 90}" y="{y + _BAR_H - 4}" '
                f'class="val muted">not scored</text>'
            )
        else:
            colour = _GOOD if val >= 0.999 else _WARN
            rows += _bar_row(y, dim, _pct(val), val, colour)
        y += _ROW_H
    return _svg(y + 4, rows, "Data quality scorecard, DAMA dimensions")


def _validation_chart(validate: dict[str, Any]) -> str:
    """Rules vs exceptions. The all-pass case renders as a clean row of
    zero-exception bars - a calm 'nothing to see', which is itself the
    finding."""
    results = validate["results"]
    total_exc = validate["total_exceptions"]
    max_fail = max((int(r["failures"]) for r in results), default=0)
    rows = ""
    y = 8
    for r in results:
        fails = int(r["failures"])
        frac = 0.0 if max_fail == 0 else fails / max_fail
        colour = _GOOD if fails == 0 else _WARN
        label = f"{r['rule_id']} {r['column']}"
        vlabel = "0 exceptions" if fails == 0 else f"{fails:,} exceptions"
        rows += _bar_row(y, label, vlabel, frac if fails else 0.0, colour)
        y += _ROW_H
    caption = f"{validate['rules_evaluated']} rules evaluated, {total_exc:,} total exceptions"
    return (
        _svg(y + 4, rows, "Validation results, exceptions per rule")
        + f'<p class="caption">{_esc(caption)}</p>'
    )


def _math_numeric_chart(numeric: dict[str, Any], skipped: list[str]) -> str:
    """Numeric column means as bars scaled to the widest column, with a
    95% CI overlay rect. All values injected from math-stage findings."""
    cols = sorted(numeric)
    max_mean = max((float(numeric[c]["mean"]) for c in cols), default=1.0) or 1.0
    bar_area = _CHART_W - 220
    rows = ""
    y = 8
    for col in cols:
        s = numeric[col]
        mean_v = float(s["mean"])
        ci_lo = float(s["mean_ci_low"])
        ci_hi = float(s["mean_ci_high"])
        n = int(s["n"])
        frac = max(0.0, min(1.0, mean_v / max_mean))
        bar_w = round(frac * bar_area)
        ci_lo_w = round(max(0.0, min(1.0, ci_lo / max_mean)) * bar_area)
        ci_hi_w = round(min(1.0, max(0.0, ci_hi / max_mean)) * bar_area)
        ci_w = max(ci_hi_w - ci_lo_w, 2)
        label = col[:19] + "…" if len(col) > 20 else col
        val_label = f"{mean_v:.4g} (n={n:,})"
        text_y = y + _BAR_H - 4
        rows += (
            f'<text x="0" y="{text_y}" class="lbl">{_esc(label)}</text>'
            f'<rect x="150" y="{y}" width="{bar_area}" height="{_BAR_H}"'
            f' fill="{_BAR_BG}" rx="3"/>'
            f'<rect x="150" y="{y}" width="{bar_w}" height="{_BAR_H}"'
            f' fill="{_ACCENT}" rx="3"/>'
            f'<rect x="{150 + ci_lo_w}" y="{y + 4}" width="{ci_w}"'
            f' height="{_BAR_H - 8}" fill="{_INK}" opacity="0.30" rx="1"/>'
            f'<text x="{_CHART_W - 60}" y="{text_y}" class="val">'
            f"{_esc(val_label)}</text>"
        )
        y += _ROW_H
    for col in sorted(skipped):
        label = col[:19] + "…" if len(col) > 20 else col
        text_y = y + _BAR_H - 4
        rows += (
            f'<text x="0" y="{text_y}" class="lbl">{_esc(label)}</text>'
            f'<rect x="150" y="{y}" width="{bar_area}" height="{_BAR_H}"'
            f' fill="none" stroke="{_GRID}" stroke-dasharray="4 3" rx="3"/>'
            f'<text x="{_CHART_W - 90}" y="{text_y}"'
            f' class="val muted">omitted</text>'
        )
        y += _ROW_H
    if not cols and not skipped:
        return '<p class="note">No numeric columns in this dataset.</p>'
    return _svg(
        y + 4,
        rows,
        "Numeric column means with 95% confidence interval",
    )


def _math_categorical_chart(categorical: dict[str, Any], skipped: list[str]) -> str:
    """Categorical column Shannon entropy as bars. entropy_normalized is
    already in [0, 1] from the findings store - no scaling required."""
    cols = sorted(categorical)
    rows = ""
    y = 8
    for col in cols:
        s = categorical[col]
        frac = max(0.0, min(1.0, float(s["entropy_normalized"])))
        bits = float(s["entropy_bits"])
        distinct = int(s["distinct"])
        label = col[:19] + "…" if len(col) > 20 else col
        val_label = f"{bits:.3g} bits / {distinct:,} cat."
        rows += _bar_row(y, label, val_label, frac, _ACCENT)
        y += _ROW_H
    for col in sorted(skipped):
        label = col[:19] + "…" if len(col) > 20 else col
        text_y = y + _BAR_H - 4
        rows += (
            f'<text x="0" y="{text_y}" class="lbl">{_esc(label)}</text>'
            f'<rect x="150" y="{y}" width="{_CHART_W - 220}"'
            f' height="{_BAR_H}" fill="none" stroke="{_GRID}"'
            f' stroke-dasharray="4 3" rx="3"/>'
            f'<text x="{_CHART_W - 90}" y="{text_y}"'
            f' class="val muted">omitted</text>'
        )
        y += _ROW_H
    if not cols and not skipped:
        return '<p class="note">No categorical columns in this dataset.</p>'
    return _svg(
        y + 4,
        rows,
        "Categorical column Shannon entropy, normalized",
    )


def _math_section(math: dict[str, Any]) -> str:
    """Assemble the Descriptive statistics HTML from math-stage findings."""
    numeric: dict[str, Any] = math.get("numeric", {})
    categorical: dict[str, Any] = math.get("categorical", {})
    skipped: list[str] = math.get("skipped", [])
    parts: list[str] = []
    if numeric or skipped:
        parts.append(
            '<p class="note">Numeric columns — bar shows mean scaled'
            " to the column with the largest mean; dark band marks the"
            " 95% confidence interval from the findings store."
            " Dashed rows were omitted from analysis.</p>"
        )
        parts.append(_math_numeric_chart(numeric, skipped))
    if categorical or skipped:
        parts.append(
            '<p class="note">Categorical columns — normalized Shannon'
            " entropy (0 = one category dominates,"
            " 1 = fully uniform)."
            " Dashed rows were omitted from analysis.</p>"
        )
        parts.append(_math_categorical_chart(categorical, skipped))
    if not parts:
        parts.append('<p class="note">No math-stage findings in this package.</p>')
    return "".join(parts)


def _profile_chart(columns: list[dict[str, Any]]) -> str:
    """Per-column completeness (share of non-null rows). Height adapts
    to the number of columns - this is the 'adaptive through the hashed
    profile' property: the data shapes the chart, by code, not by an AI
    deciding anything."""
    rows = ""
    y = 8
    for col in columns:
        comp = float(col["completeness"])
        colour = _GOOD if comp >= 0.999 else _WARN
        label = str(col["name"])
        if len(label) > 20:
            label = label[:19] + "\u2026"
        rows += _bar_row(y, label, _pct(comp), comp, colour)
        y += _ROW_H
    return _svg(y + 4, rows, "Column completeness, share of non-null rows")


