def _profile_table(columns: list[dict[str, Any]]) -> str:
    """The column profile as a proper table - the exact hashed values,
    scannable. Complements the completeness chart above it."""
    body = ""
    for c in columns:
        body += (
            "<tr>"
            f"<td>{_esc(c['name'])}</td>"
            f"<td>{_esc(c['dtype'])}</td>"
            f"<td class='num'>{int(c['total']):,}</td>"
            f"<td class='num'>{int(c['nulls']):,}</td>"
            f"<td class='num'>{int(c['distinct']):,}</td>"
            f"<td class='num'>{_pct(float(c['valid_ratio']))}</td>"
            "</tr>"
        )
    return (
        "<table><thead><tr>"
        "<th>Column</th><th>Type</th><th>Rows</th><th>Nulls</th>"
        "<th>Distinct</th><th>Validity</th>"
        "</tr></thead><tbody>" + body + "</tbody></table>"
    )


def build_report_html(
    profile: dict[str, Any],
    validate: dict[str, Any],
    source: str,
    profile_digest: str,
    validate_digest: str,
    generated_date: str = "",
    math: dict[str, Any] | None = None,
    math_digest: str = "",
) -> str:
    """Render the full self-contained HTML report from findings.

    A pure function: the same arguments always produce byte-identical
    output. Every number shown is taken directly from `profile`,
    `validate`, or `math`; nothing is computed here beyond scaling
    values to pixel widths and formatting them for display.

    `generated_date` is optional display metadata for the footer. It is
    the ONLY value that varies between runs, and it lives outside the
    findings region: the report's content stays a deterministic
    function of the findings, while the date honestly records when the
    file was rendered. Pass a fixed string for byte-stable output.

    `math` is the findings dict from math.json; when None the
    Descriptive statistics section is omitted entirely.
    """
    dama = profile["dama_scores"]
    columns = profile["columns"]

    doc = f"""<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Data review - {_esc(source)}</title>
<style>{_CSS}</style>
</head>
<body>
<main class="wrap">
<header>
<h1>Data review</h1>
<p class="sub">Source: {_esc(source)}</p>
</header>

<p class="lede">Every figure below was computed by the deterministic
engine and drawn directly from the hashed findings store. These charts
are a starting point for your own review - they show the shape of the
data so you can decide where to look, not what to conclude.</p>

<section>
<h2>Data quality scorecard</h2>
<p class="note">DAMA six dimensions. Accuracy and timeliness are shown
as "not scored" where the dataset alone cannot establish them - absence
of a score is never drawn as a score of zero.</p>
{_dama_chart(dama)}
</section>

<section>
<h2>Validation results</h2>
<p class="note">Exceptions per rule. Exceptions are reported, never
dropped - every failure is evidence.</p>
{_validation_chart(validate)}
</section>

<section>
<h2>Column completeness</h2>
<p class="note">Share of non-null rows per column. The chart height
follows the data - it adapts to however many columns the profile
found.</p>
{_profile_chart(columns)}
</section>

<section>
<h2>Column profile</h2>
<p class="note">The exact profiled values behind the chart above.</p>
{_profile_table(columns)}
</section>

{
        f'''<section>
<h2>Descriptive statistics</h2>
<p class="note">Distribution summary from the math-stage findings.
Every value traces to math.json in the findings store.</p>
{_math_section(math)}
</section>'''
        if math is not None
        else ""
    }

<footer>
{f"<p>Report generated: {_esc(generated_date)}</p>" if generated_date else ""}
<p>Provenance and verification. Recompute the SHA-256 of each findings
file and compare against these digests; every number in this report
traces to them.</p>
<p>dq_profile: <code>{_esc(profile_digest)}</code></p>
<p>dq_validate: <code>{_esc(validate_digest)}</code></p>
{f"<p>math: <code>{_esc(math_digest)}</code></p>" if math_digest else ""}
<p class="sig">Rendered deterministically by the Delivery Engine from
the hashed findings store. No figure was computed, estimated, or
authored by an AI - the report draws the evidence, the reader supplies
the judgement.</p>
</footer>
</main>
</body>
</html>
"""
    return doc


def report_from_package(package_dir: str) -> Path:
    """Read a sealed package's findings and write report.html into it.

    Works from the committed JSON files, so it runs on any package
    directory without a live store. Returns the path written.
    """
    pkg = Path(package_dir)
    if not pkg.is_dir():
        raise ReportError(f"Not a directory: {package_dir}")
    findings = pkg / "findings"
    prof_path = findings / "dq_profile.json"
    val_path = findings / "dq_validate.json"
    for p in (prof_path, val_path):
        if not p.exists():
            raise ReportError(
                f"Expected findings file not found: {p}. The report is "
                f"built from a sealed package's dq_profile and "
                f"dq_validate findings."
            )
    prof_doc = json.loads(prof_path.read_text(encoding="utf-8"))
    val_doc = json.loads(val_path.read_text(encoding="utf-8"))

    # Findings files store the payload under "findings" with the digest
    # alongside; fall back gracefully if given a bare findings dict.
    profile = prof_doc.get("findings", prof_doc)
    validate = val_doc.get("findings", val_doc)
    prof_digest = str(prof_doc.get("sha256", ""))
    val_digest = str(val_doc.get("sha256", ""))

    # Recover the source label if the manifest records it; else neutral.
    source = "the analysed dataset"
    manifest = pkg / "manifest.json"
    if manifest.exists():
        m = json.loads(manifest.read_text(encoding="utf-8"))
        fp = m.get("source_fingerprint", {})
        if isinstance(fp, dict) and fp.get("path"):
            source = str(fp["path"])

    # Load optional math.json — present when the math stage has run.
    math_path = findings / "math.json"
    math_findings: dict[str, Any] | None = None
    math_digest = ""
    if math_path.exists():
        math_doc = json.loads(math_path.read_text(encoding="utf-8"))
        math_findings = math_doc.get("findings", math_doc)
        math_digest = str(math_doc.get("sha256", ""))

    # The generation date is display metadata, injected here (not in
    # the pure builder) so build_report_html stays byte-deterministic.
    from datetime import date

    generated = date.today().strftime("%d %B %Y")
    doc_html = build_report_html(
        profile,
        validate,
        source,
        prof_digest,
        val_digest,
        generated,
        math=math_findings,
        math_digest=math_digest,
    )
    out = pkg / "report.html"
    out.write_text(doc_html, encoding="utf-8")
    return out
